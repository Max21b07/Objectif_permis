const l = (vi: string, en: string, fr: string): LocalizedString => ({ vi, en, fr });

function makeQuestion(
  id: string,
  question: LocalizedString,
  correct: LocalizedString,
  wrong1: LocalizedString,
  wrong2: LocalizedString,
  explanation: LocalizedString,
  level: QuizQuestion["level"],
  category: string,
): QuizQuestion {
  return {
    id,
    question,
    answers: [correct, wrong1, wrong2],
    correctIndex: 0,
    explanation,
    level,
    category,
    language: "all",
  };
}

